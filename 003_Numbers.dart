void main() {
  int num1 = 12;

  print(num1);

  int num2 = 38;

  print(num2);

  print(num1 + num2);
  print(num1 - num2);
  print(num1 * num2);
  print(num1 / num2);

  print("--------------");

  double num3 = 2.5;
  double num4 = 0.5;

  print(num3);
  print(num4);

  // double num5 = 12;
  // int num6 = 2.5;
  // double형식에 정수 값을 저장할 수는 있지만, int형식에 실수 값은 저장할 수 없다.

  print(num3 + num4);
  print(num3 - num4);
  print(num3 * num4);
  print(num3 / num4);
}
